#Solicita al usario que ingrese dos numeros enteros.
a = int(input("Ingresa el primero numero: "))
b = int(input("Ingresa el segundo numero: "))

#Muestra la suma, la diferencia, el producto y el cociente de los numero ingresados.
print("Suma: ", a + b)
print("Diferencia: ", a - b)
print("Producto: ", a * b)
print("Cociente: ", a / b)
#Observacion: Este resultado puede devolver un valor decimal
